import SwiftUI

extension Font {
    static func PatuaOne(size: CGFloat) -> Font {
        return Font.custom("PatuaOne-Regular", size: size)
    }
    static func VarelaRound(size: CGFloat) -> Font {
        return Font.custom("VarelaRound-Regular", size: size)
    }
}
